main() {
  // Operadores Aritméticos
  int a = 7;
  int b = 3;
  int resultado = a + b;

  print(resultado);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  // Operadores Lógicos
  bool fragil = true;
  bool caro = false;

  print(fragil && caro); // And
  print(fragil || caro); // Or
  print(fragil ^ caro); // Xor
  print(!fragil); // Not   -> Unário, pré-fixado
  print(!caro);
}
